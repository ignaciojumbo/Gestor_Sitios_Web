    private void buscarPaginas() {
        System.out.println("Combo" + cmbbusquedad.getSelectedIndex());
        switch (cmbbusquedad.getSelectedIndex()) {
            case 1://Nombre
                tablaPagina.setListaPagina(controladorpagina.buscarPaginasnombre(txtbusquedad.getText()));
                tablaPagina.fireTableDataChanged();
                break;
            case 2://categoria
                tablaPagina.setListaPagina(controladorpagina.buscarPaginasCategoria(txtbusquedad.getText()));
                tablaPagina.fireTableDataChanged();
                break;
        }

    }
