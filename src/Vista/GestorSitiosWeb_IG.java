    @Override
    public void clickPagina(Pagina p) {
        txtnombrepagina.setText(p.getNombrePagina());
        txtlinkpagina.setText(p.getLinkPagina());
        cbxtipoPagina.setSelectedItem(p.getTipoPagina());
        txtusuario.setText(p.getUsuario());
        txtclave.setText(utilidad.Desencriptadoclave(p.getClave()));
        pagina = p;
        bnguardar.setEnabled(false);
        bneditar.setEnabled(true);
        bneliminar.setEnabled(true);
        abrirEnlace(p);

    }

    int contadorClick = 0;
    boolean ejecutar;

// este metodo sirve para cuando se de dos click en la tabla este abra el enlace
    public void abrirEnlace(Pagina pd) {
        ejecutar = true;
        //HILO DE PROGRMACIÓN
        new Thread(new Runnable() {
            @Override
            public void run() {
                while (ejecutar) {
                    try {
                        //Parar un segundo
                        Thread.sleep(1000);
                        contadorClick = 0;
                        ejecutar = false;
                        System.out.println("Reinicia CLICK");
                    } catch (InterruptedException e) {
                        System.out.println(e.getMessage());
                    }
                }
            }
        }).start();

        contadorClick++;
        if (contadorClick == 3) {
            if (JOptionPane.showConfirmDialog(rootPane, "Se abrira el enlace. Desea continuar",
                    "Se abrira el enlace", JOptionPane.INFORMATION_MESSAGE, JOptionPane.YES_NO_OPTION)
                    == JOptionPane.YES_OPTION) {
                //esto sirve para abrir el enlace
                Desktop enlace = Desktop.getDesktop();
                String enlaceAcceder = pagina.getLinkPagina();
                try {
                    enlace.browse(new URI(enlaceAcceder));
                } catch (IOException | URISyntaxException e) {
                    e.getMessage();
                }
                contadorClick = 0;
                ejecutar = false;
            }

        }
        System.out.println("contadorClick" + contadorClick);
    }

